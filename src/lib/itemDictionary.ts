export function getCategoryFromDictionary(itemName: string): string {
  const trimmed = itemName.trim()
  if (!trimmed) return 'General'
  const lower = trimmed.toLowerCase()
  for (const [key, category] of Object.entries(ITEM_DICTIONARY)) {
    if (lower === key.toLowerCase() || lower.includes(key.toLowerCase())) return category
  }
  return 'General'
}
